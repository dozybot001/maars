"""Team 阶段（Refine + Write）全部 prompt — 中文版。"""

_PREFIX = (
    "这是一个全自动流水线，无人参与。"
    "不要提问或请求输入，自主做出所有决策。\n"
    "所有输出使用中文撰写。\n\n"
)

_REVIEWER_OUTPUT_FORMAT = """

在你的分析之后，输出一个 JSON 块作为结构化评估：

```json
{
  "issues": [
    {"section": "章节名称", "problem": "问题描述", "suggestion": "修复建议"}
  ],
  "resolved": ["I1", "I3"]
}
```

JSON 块规则：
- 当 "issues" 列表为空时，系统自动放行。你不需要决定是否通过——只需如实报告你发现的问题。
- 如果一切良好，输出空列表："issues": []。
- issues 中不要包含 "id" 字段——系统会自动分配 ID。
- 只报告你在本轮发现的新问题。不要回显之前已识别的问题。
- "resolved"：仅列出"此前已识别的问题"章节中显示的系统 ID（如 "I1"、"I2"）。\
如果没有此前的问题，省略 "resolved" 或设为 []。
- 你必须输出此 JSON 块——流水线依赖它来跟踪进度。"""

